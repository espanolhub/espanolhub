// Helper functions
export function getQuestionsByLevel(level: 'beginner' | 'intermediate' | 'advanced'): VerbQuizQuestion[] {
  return verbQuizQuestions.filter(q => q.level === level);
}

export function getQuestionsByCategory(category: string): VerbQuizQuestion[] {
  return verbQuizQuestions.filter(q => q.category === category);
}

export function getRandomQuestions(count: number, level?: 'beginner' | 'intermediate' | 'advanced'): VerbQuizQuestion[] {
  let pool = level ? getQuestionsByLevel(level) : verbQuizQuestions;
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getAllQuestions(): VerbQuizQuestion[] {
  return verbQuizQuestions;
}

/** Genera explicación en español para mostrar en la UI cuando falta explanation_es */
export function getExplanationEs(q: VerbQuizQuestion): string {
  const cat = q.category;
  const v = q.verb;
  const a = q.correctAnswer;
  const ex = q.exampleSentence;
  if (cat === 'presente') {
    return `El verbo "${v}" en presente: "${a}". Ejemplo: ${ex}`;
  }
  if (cat === 'preterito') {
    return `En pretérito, "${v}" → "${a}". Ejemplo: ${ex}`;
  }
  if (cat === 'futuro') {
    return `En futuro, "${v}" → "${a}". Ejemplo: ${ex}`;
  }
  if (cat === 'imperfecto') {
    return `En imperfecto, "${v}" → "${a}". Ejemplo: ${ex}`;
  }
  if (cat === 'condicional') {
    return `En condicional, "${v}" → "${a}". Ejemplo: ${ex}`;
  }
  if (cat === 'reflexivos') {
    return `Verbo reflexivo "${v}": "${a}". Ejemplo: ${ex}`;
  }
  if (cat === 'irregulares') {
    return `Verbo irregular "${v}": "${a}". Ejemplo: ${ex}`;
  }
  return `Respuesta correcta: "${a}". Ejemplo: ${ex}`;
}
