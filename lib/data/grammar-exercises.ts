/**
 * Shuffle array using Fisher-Yates algorithm
 * خلط المصفوفة باستخدام خوارزمية Fisher-Yates
 */
function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

/**
 * Get random exercises from a pool
 * الحصول على تمارين عشوائية من مجموعة
 * @param exercises - All available exercises
 * @param count - Number of exercises to select (default: 15)
 * @returns Randomly selected exercises
 */
export function getRandomExercises(
  exercises: GrammarExercise[],
  count: number = 15
): GrammarExercise[] {
  if (exercises.length <= count) {
    // If we have fewer or equal exercises, return all shuffled
    return shuffleArray(exercises);
  }
  
  // Shuffle and take first 'count' exercises
  const shuffled = shuffleArray(exercises);
  return shuffled.slice(0, count);
}
