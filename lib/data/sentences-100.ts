/**
 * Shuffle array using Fisher-Yates algorithm
 * خلط المصفوفة باستخدام خوارزمية Fisher-Yates
 */
function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

/**
 * Get random sentences from a pool
 * الحصول على جمل عشوائية من مجموعة
 * @param sentences - All available sentences
 * @param count - Number of sentences to select (default: 20)
 * @returns Randomly selected sentences
 */
export function getRandomSentences(
  sentences: Sentence[],
  count: number = 20
): Sentence[] {
  if (sentences.length <= count) {
    // If we have fewer or equal sentences, return all shuffled
    return shuffleArray(sentences);
  }
  
  // Shuffle and take first 'count' sentences
  const shuffled = shuffleArray(sentences);
  return shuffled.slice(0, count);
}
