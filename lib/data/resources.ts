export type LibraryCategory = 'free-guides' | 'workbooks' | 'grammar-cheat-sheets' | 'all';

export function getResourcesByCategory(category: Resource['category']): Resource[] {
  return resources.filter(r => r.category === category);
}

export function getResourcesByPrice(price: ResourcePrice): Resource[] {
  return resources.filter(r => r.price === price);
}

export function getResourcesByLibraryCategory(libraryCategory: LibraryCategory): Resource[] {
  if (libraryCategory === 'all') return resources;
  return resources.filter(r => (r as any).libraryCategory === libraryCategory);
}

export function getResourceById(id: string): Resource | undefined {
  return resources.find(r => r.id === id);
}

export function getFreeResources(): Resource[] {
  return getResourcesByPrice('free');
}

export function getPaidResources(): Resource[] {
  return getResourcesByPrice('paid');
}

export function searchResources(query: string): Resource[] {
  const lowerQuery = query.toLowerCase();
  return resources.filter(resource => 
    resource.title.toLowerCase().includes(lowerQuery) ||
    resource.description.toLowerCase().includes(lowerQuery) ||
    (resource.titleAr && resource.titleAr.includes(lowerQuery)) ||
    (resource.descriptionAr && resource.descriptionAr.toLowerCase().includes(lowerQuery)) ||
    resource.tags?.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}
